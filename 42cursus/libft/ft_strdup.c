#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*a;
	char	*b;
	size_t	i;
	size_t	c;

	a = s1;
	i = ft_strlen (a);
	b = malloc(i + 1);
	c = 0;
	while (a[c])
	{
		a[c] = b[c];
		c++;
	}
	b[c] = '\0';
	return (b);
}
